package com.creachy.hsfindarchetype;

public class Card {
    Integer dbfId;
    String name;
    String text;

    public Integer getDbfId() {
        return dbfId;
    }

    public void setDbfId(Integer dbfId) {
        this.dbfId = dbfId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
